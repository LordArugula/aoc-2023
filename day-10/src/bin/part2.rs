fn get_area(path: &Vec<(usize, usize)>, map: &Vec<Vec<char>>) -> u32 {
  let path_lookup = path.iter()
    .map(|x| *x)
    .collect::<std::collections::HashSet<_>>();

  let mut area_inside_path = std::collections::HashSet::<_>::new();

  path.windows(2)
    .for_each(|pair| {
      let (row_a, col_a) = pair[0];
      let (row_b, col_b) = pair[1];

      if row_a > row_b {
        // moving down, check right
        if col_a < map[row_a].len() - 1 && !path_lookup.contains(&(row_a, col_a + 1)) {
          area_inside_path.insert((row_a, col_a + 1));
        }
        if col_b < map[row_b].len() - 1 && !path_lookup.contains(&(row_b, col_b + 1)) {
          area_inside_path.insert((row_b, col_b + 1));
        }
      } 
      else if row_a < row_b{
        // moving up, check left
        if col_a > 0 && !path_lookup.contains(&(row_a, col_a - 1)) {
          area_inside_path.insert((row_a, col_a - 1));
        }
        if col_b > 0 && !path_lookup.contains(&(row_b, col_b - 1)) {
          area_inside_path.insert((row_b, col_b - 1));
        }
      } 
      else if col_a < col_b {
        // moving right, check up
        if row_a < map.len() - 1 && !path_lookup.contains(&(row_a + 1, col_a)) {
          area_inside_path.insert((row_a + 1, col_a));
        }
        if row_b < map.len() - 1 && !path_lookup.contains(&(row_b + 1, col_b)) {
          area_inside_path.insert((row_b + 1, col_b));
        }
      } 
      else if col_a > col_b {
        // moving left, check down
        if row_a > 0 && !path_lookup.contains(&(row_a - 1, col_a)) {
          area_inside_path.insert((row_a - 1, col_a));
        }
        if row_b > 0 && !path_lookup.contains(&(row_b - 1, col_b)) {
          area_inside_path.insert((row_b - 1, col_b));
        }
      }
    });

  let mut open_space = Vec::from_iter(area_inside_path.iter().map(|&pos| pos));

  while let Some((row, col)) = open_space.pop() {
    path::get_neighbors((row, col), &map)
      .iter()
      .filter_map(|&neighbor| neighbor)
      .for_each(|neighbor| { 
        if path_lookup.contains(&neighbor) 
          || area_inside_path.contains(&neighbor) {
          return;
        }
        open_space.push(neighbor);
        area_inside_path.insert(neighbor);
      });
  }
  area_inside_path.len() as u32
}

fn part_two(input: &str) -> u32 {
  let map = path::parse_map(input);
  let mut path = path::find_path(&map);

  let area_clockwise = get_area(&path, &map);
  path.reverse();
  let area_counterclockwise = get_area(&path, &map);
  
  std::cmp::min(area_counterclockwise, area_clockwise)
}

